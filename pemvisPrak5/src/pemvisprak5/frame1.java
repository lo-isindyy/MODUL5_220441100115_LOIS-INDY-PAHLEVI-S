    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        nama=tfNama.getText();
        String totalBayarText = tfBayar.getText().trim(); // Dapatkan teks dan hapus spasi
        if (totalBayarText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan pembayaran dengan benar. Harap isi total bayar.");
        }else{
            bayar = Integer.parseInt(totalBayarText);
            if(bayar==0||bayar<total_harga){
            JOptionPane.showMessageDialog(null, "Pembayaran anda kurang, Tolong Masukan Pembayaran dengan sesuai!!");
            }else{
                kembali = bayar-total_harga;
                tfKembalian.setText(""+kembali);
            }                                      
        }
    }//GEN-LAST:event_btnBayarActionPerformed

