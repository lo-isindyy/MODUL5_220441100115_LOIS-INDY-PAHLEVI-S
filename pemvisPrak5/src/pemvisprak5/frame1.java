    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (btnSimpan.getText().equals("Simpan")) {
            if (barang==null||kode==null||harga==0||beli==0||total_harga==0||bayar==0) {
                JOptionPane.showMessageDialog(null, "Tolong Input Data dengan Benar");
                  reset();
            }else{
                try{
                    String sql="insert into tb_datapenjualan values('"+barang+"','"+kode+"','"+harga+"','"+tfNama.getText()+"','"+beli+"','"+total_harga+"','"+bayar+"','"+kembali+  "')";
                    java.sql.Connection conn=(java.sql.Connection)pemvisprak5.koneksi.koneksiDB();
                    java.sql.PreparedStatement 
                    pst=conn.prepareStatement(sql);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                    reset();
                    tampil_data();
                    list1.addElement(nama + " telah Membeli Barang Dengan Kode " + kode);
                }
                catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Gagal disimpan");
                    System.out.println(e.getMessage());
                }
            }
        }else if (btnSimpan.getText().equals("Edit")){
            try{
                String update ="UPDATE tb_datapenjualan SET harga_barang='"+harga+"',kode_barang='"+kode+"',nama_barang='"+barang+"',jumlah_beli='"+beli+"',total_harga='"+total_harga+"',total_bayar='"+bayar+"',kembalian='"+kembali+"' where nama_pembeli='"+tfNama.getText()+"'";
                java.sql.Connection conn=(java.sql.Connection)pemvisprak5.koneksi.koneksiDB();
                java.sql.PreparedStatement 
                pst=conn.prepareStatement(update);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Edit Berhasil disimpan");
                reset();
                tampil_data();
                 list1.addElement( "Data pembelian dengan kode " + kode + " telah DiEdit");
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Gagal disimpan");
                System.out.println(e.getMessage());
            }
        }
        list1.addElement(nama + " telah Membeli Barang Dengan Kode " + kode);
    }//GEN-LAST:event_btnSimpanActionPerformed

